// Function to apply translations to the page
function applyTranslations(lang) {
    if (!translations[lang]) {
        console.error(`Language ${lang} not supported`);
        return;
    }
    
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });
    
    // Update language toggle active state
    const languageToggles = document.querySelectorAll('.language-toggle a');
    languageToggles.forEach(toggle => {
        toggle.classList.remove('active');
        if (toggle.textContent.toLowerCase() === lang) {
            toggle.classList.add('active');
        }
    });
    
    // Store language preference
    localStorage.setItem('language', lang);
}

// Function to initialize language based on user preference or browser settings
function initializeLanguage() {
    // Check if user has a stored preference
    const storedLang = localStorage.getItem('language');
    if (storedLang && translations[storedLang]) {
        applyTranslations(storedLang);
        return;
    }
    
    // Check browser language
    const browserLang = navigator.language.split('-')[0];
    if (translations[browserLang]) {
        applyTranslations(browserLang);
        return;
    }
    
    // Default to English
    applyTranslations('en');
}

// Add event listeners for language toggles
document.addEventListener('DOMContentLoaded', function() {
    // Initialize language
    initializeLanguage();
    
    // Add click handlers for language toggles
    const languageToggles = document.querySelectorAll('.language-toggle a');
    languageToggles.forEach(toggle => {
        toggle.addEventListener('click', function(e) {
            e.preventDefault();
            const lang = this.textContent.toLowerCase();
            applyTranslations(lang);
        });
    });
});
